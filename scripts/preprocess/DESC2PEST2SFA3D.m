function plotFields3D_(fields,field_titles,name,phiIndex)
N_phi = size(fields{1},3);
n_fields = numel(fields);
slider_max = max(N_phi,2);
slider_step = [1/(slider_max-1),min(1,5/(slider_max-1))];
if n_fields <= 3
    n_rows = 1;
    n_cols = n_fields;
else
    n_cols = ceil(sqrt(n_fields));
    n_rows = ceil(n_fields/n_cols);
end

fig = figure('Name',name);
plot_panel = uipanel('Parent',fig,'Position',[0,0.12,1,0.88],'BorderType','none');
phi_text = uicontrol('Parent',fig,'Style','text','Units','normalized', ...
    'Position',[0.42,0.075,0.16,0.03]);
phi_slider = uicontrol('Parent',fig,'Style','slider','Units','normalized', ...
    'Position',[0.18,0.03,0.64,0.04],'Min',1,'Max',slider_max, ...
    'Value',phiIndex,'SliderStep',slider_step,'Callback',@updatePhi_);

axis_handles = gobjects(1,n_fields);
mesh_handles = gobjects(1,n_fields);
for i=1:n_fields
    axis_handles(i) = subplot(n_rows,n_cols,i,'Parent',plot_panel);
    mesh_handles(i) = mesh(axis_handles(i),fields{i}(:,:,phiIndex));
end
updatePhi_(phi_slider,[]);

    function updatePhi_(~,~)
    phiIndex = round(get(phi_slider,'Value'));
    phiIndex = max(1,min(N_phi,phiIndex));
    set(phi_slider,'Value',phiIndex);
    set(phi_text,'String',['phi index = ',num2str(phiIndex),' / ',num2str(N_phi)]);
    for j=1:n_fields
        set(mesh_handles(j),'ZData',fields{j}(:,:,phiIndex),'CData',fields{j}(:,:,phiIndex));
        title(axis_handles(j),[field_titles{j},', phi=',num2str(phiIndex)]);
    end
    drawnow limitrate;
    end
end
