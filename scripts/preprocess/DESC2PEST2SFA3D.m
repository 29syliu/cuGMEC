function plotCompare3D_(direct,transform,name)
relErr = (transform-direct)./direct;
errPerPhi = squeeze(max(max(abs(relErr),[],1),[],2));
[~,phiIndex] = max(errPerPhi);

plotFields3D_({direct,transform,relErr}, ...
    {[name,' (Direct)'],[name,' (Transform)'],[name,' Relative Error']}, ...
    name,phiIndex);
end

